package com.tfedorov.social.intention;

public class IntentString {

	private String resultHTML;

	private boolean isIntention;

	/**
	 * @return the resultHTML
	 */
	public String getResultHTML() {
		return resultHTML;
	}

	/**
	 * @param resultHTML
	 *            the resultHTML to set
	 */
	public void setResultHTML(String resultHTML) {
		this.resultHTML = resultHTML;
	}

	/**
	 * @return the isIntention
	 */
	public boolean isIntention() {
		return isIntention;
	}

	/**
	 * @param isIntention
	 *            the isIntention to set
	 */
	public void setIntention(boolean isIntention) {
		this.isIntention = isIntention;
	}

}
